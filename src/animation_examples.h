namespace Physics
{
    // Estrutura para armazenar estado físico completo
    struct BodyState
    {
        glm::vec3 position;
        glm::vec3 velocity;
        float mass;
    };

    // Integrador de Euler (simples, mas impreciso para longo prazo)
    void EulerIntegrator(std::vector<BodyState>& states, float dt)
    {
        std::vector<glm::vec3> accelerations(states.size(), glm::vec3(0.0f));
        
        // Calcula acelerações (força gravitacional newtoniana)
        for (size_t i = 0; i < states.size(); i++)
        {
            for (size_t j = 0; j < states.size(); j++)
            {
                if (i == j) continue;
                
                glm::vec3 r_vec = states[j].position - states[i].position;
                float r = glm::length(r_vec);
                
                if (r < 1e-6f) continue; // Evita divisão por zero
                
                // F = G * m1 * m2 / r²  (G = 1 em unidades geométricas)
                // a = F / m = m_j / r²
                float acc_magnitude = states[j].mass / (r * r);
                accelerations[i] += acc_magnitude * glm::normalize(r_vec);
            }
        }
        
        // Atualiza velocidades e posições
        for (size_t i = 0; i < states.size(); i++)
        {
            states[i].velocity += accelerations[i] * dt;
            states[i].position += states[i].velocity * dt;
        }
    }

    // Converte BodyState ? glm::vec4 para envio ao shader
    void StatesToBodies(const std::vector<BodyState>& states, std::vector<glm::vec4>& bodies)
    {
        bodies.resize(states.size());
        for (size_t i = 0; i < states.size(); i++)
        {
            bodies[i] = glm::vec4(states[i].position, states[i].mass);
        }
    }
}

